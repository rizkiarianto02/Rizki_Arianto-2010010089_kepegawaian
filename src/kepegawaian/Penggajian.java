    private void txt_id_penggajianKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_id_penggajianKeyPressed
        // TODO add your handling code here:
        id_penggajian=txt_id_penggajian.getText(); 
        int tekanenter=evt.getKeyCode();
        if (tekanenter==10){
            try{
                sql="select * from tb_penggajian "
                        + "where id_penggajian='"+ id_penggajian + "'";
                stat=con.createStatement();
                res=stat.executeQuery(sql);
                while (res.next()){
                    txt_nama_karyawan.setText(res.getString("nama_pegawai"));
                    txt_id_golongan.setText(res.getString("id_golongan"));
                    txt_gaji_pokok.setText(res.getString("gaji_pokok"));
                    txt_tunjangan_gaji.setText(res.getString("tunjangan_gaji"));
                     txt_total_gaji.setText(res.getString("total_gaji"));
                    JOptionPane.showMessageDialog(null, "Data Ditemukan");
                }
            }catch (Exception e){
                JOptionPane.showMessageDialog(null, "Tidak Ditemukan \n"+e.getMessage());
                txt_nama_karyawan.requestFocus();
            }
        }
    }//GEN-LAST:event_txt_id_penggajianKeyPressed

