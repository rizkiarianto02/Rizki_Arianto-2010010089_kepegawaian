private void TampilData (String sql){
    DefaultTableModel t = new DefaultTableModel();
    
    t.addColumn("id_penggajian");
    t.addColumn("nama_pegawai");
    t.addColumn("id_golongan");
    t.addColumn("gaji_pokok");
    t.addColumn("tunjangan_gaji");
    t.addColumn("total_gaji");
    Table.setModel(t);
    try {
        int i = 1;
        stat=con.createStatement();
        res=stat.executeQuery("select * from tb_penggajian");
        while (res.next()) {
            t.addRow(new Object[]{
            res.getString("id_penggajian"),
            res.getString("nama_pegawai"),
            res.getString("id_golongan"),
            res.getString("gaji_pokok"),
            res.getString("tunjangan_gaji"),
            res.getString("total_gaji")
            });
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
    }
}



